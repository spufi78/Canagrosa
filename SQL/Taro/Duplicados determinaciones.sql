SELECT A.ID_GENERAL,A.ANNO, A.FECHA_RECEPCION, TD.NOMBRE, FC.NOMBRE, C.VALOR_1, C.VALOR_2
  FROM MUESTRAS A, DETERMINACIONES B, DATOS_DETERMINACIONES C, FORMULAS F, TIPOS_DETERMINACION TD, FORMULAS_CAMPOS FC
 WHERE A.ANALISIS_DUPLICADO = 1
   AND A.ID_MUESTRA = B.MUESTRA_ID
   AND B.FORMULA_ID = F.ID_FORMULA
   AND B.ID_DETERMINACION = C.DETERMINACION_ID
	AND C.CAMPO_ID = FC.ID_CAMPO
--	AND C.CAMPO_ID = F.CAMPO_ID_RESULTADO   
	AND B.TIPO_DETERMINACION_ID = TD.ID_TIPO_DETERMINACION
   AND A.FECHA_RECEPCION >= '2019-07-01' 
--	AND A.FECHA_RECEPCION <= '2013-12-31'
   AND B.TIPO_DETERMINACION_ID IN (1137,1467,1544,1701,1702
   ,2718, 2394, 3258, 2516, 3511, 2393, 4195, 2244, 2382, 2517, 2243, 2381, 2518, 2372, 2383, 2921, 1098
   ,3898, 2403, 3899, 2404, 2384, 2266, 2371, 3570, 3571, 2351, 2385, 3312, 3313, 2930, 2929, 3527
   ,1577,1589
   )
ORDER BY 1,2,C.CAMPO_ID

