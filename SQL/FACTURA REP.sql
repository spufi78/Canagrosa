SELECT DISTINCT A.DOC_ID , B.DOC_ID 
FROM DOCS_PAGO_MUESTRAS A,DOCS_PAGO_MUESTRAS B, DOCS_PAGO C, DOCS_PAGO D
WHERE A.DOC_ID <> B.DOC_ID
AND A.MUESTRA_ID = B.MUESTRA_ID 
AND A.DOC_ID = C.ID_DOC AND C.TIPO = 2 AND C.ANULADO = 0
AND B.DOC_ID = D.ID_DOC AND D.TIPO = 2 AND D.ANULADO = 0
AND A.DOC_ID > 9000
AND B.DOC_ID > 9000
AND A.MUESTRA_ID <> 0 

