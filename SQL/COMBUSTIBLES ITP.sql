SELECT A.ID_GENERAL, A.FECHA_RECEPCION, TD.NOMBRE, C.VALOR_1, C.VALOR_2
  FROM MUESTRAS A, DETERMINACIONES B, DATOS_DETERMINACIONES C, FORMULAS F, TIPOS_DETERMINACION TD
 WHERE A.ANALISIS_DUPLICADO = 1
   AND A.ID_MUESTRA = B.MUESTRA_ID
   AND B.FORMULA_ID = F.ID_FORMULA
   AND B.ID_DETERMINACION = C.DETERMINACION_ID
	AND C.CAMPO_ID = F.CAMPO_ID_RESULTADO   
	AND B.TIPO_DETERMINACION_ID = TD.ID_TIPO_DETERMINACION
   AND A.FECHA_RECEPCION >= '2013-06-01' AND A.FECHA_RECEPCION <= '2013-11-30'
   AND B.TIPO_DETERMINACION_ID IN (1678,1137,1923,1924,2120,2121,2119,1925,2122,2123,1926,2124,1928,1927,1922,1982,1138,1464,1544,1515)